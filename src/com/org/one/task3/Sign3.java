public class Sign3 {

    private int posittion;
    private int number;

    public Sign3(int posittion, int number) {
        this.posittion = posittion;
        this.number = number;
    }

    public int getPosittion() {
        return posittion;
    }

    public void setPosittion(int posittion) {
        this.posittion = posittion;
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }
}
